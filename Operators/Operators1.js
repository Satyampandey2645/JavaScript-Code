// Arithmetic Operators in Js
// +, -, *, /, %, **, ++, -- 

let a = 6;
let b = 4;
console.log("a + b = ", a+b);  // 10
console.log("a - b = ", a-b);  // 2
console.log("a * b = ", a*b);  // 24
console.log("a / b = ", a/b);  // 1.5
console.log("a % b = ", a%b);  // 2
console.log("a ** b = ", a**b);  // 1296
console.log("a++ = ", a++);  // 6
console.log("a = ", a);  // 7
console.log("b-- = ", b--);  // 4
console.log("b = ", b);  // 3

let x = 6;
let y = 4;
console.log("++x = ", ++x);  // 7
console.log("x = ", x);  // 7
console.log("--y = ", --y);  // 3
console.log("y = ", y);  // 3
