// Assignment Operators in Js
// =, +=, -=, *=, /=, %=, **=

let a = 11;
a += 5;  // same as a = a + 5
console.log (a)  // 16
a -= 5;  // same as a = a - 5
console.log (a)  // 11
a *= 5;  // same as a = a * 5
console.log (a)  // 55
a /= 5;  // same as a = a / 5
console.log (a)  // 11
a %= 5;  // same as a = a % 5
console.log (a)  // 1
a **= 5;  // same as a = a ** 5
console.log (a)  // 1
